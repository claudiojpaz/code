#include <stdio.h>

int main (void) {
  int numero = 8;

  switch (numero) {
    case 1:
      printf("uno\n");
      break;
    case 0:
      printf("cero\n");
      break;
    case 2:
      printf("dos\n");
      break;
    case 3:
      printf("tres\n");
      break;
    case 4:
      printf("cuatro\n");
      break;
  }

  return 0;
}

